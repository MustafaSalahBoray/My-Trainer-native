                <h2>Side Effects</h2>
                <p class="text-danger fs-5">
                    For the most part, BCAAs are harmless, and most experts suggest
                    that habitual (daily) supplementation increases their
                    effectiveness. However, as with anything, excess use can have
                    potential negative side effects. When consumed in large amounts,
                    BCAA side effects can include fatigue, loss of coordination,
                    nausea, headaches, and increased insulin resistance (which can
                    lead to Type 2 diabetes). BCAAs may affect blood sugar levels, so
                    anyone having surgery should avoid them for a period of time
                    before and after surgery.
                    <br />
                    BCAAs are also contraindicated for people with certain conditions.
                    Although BCAAs were once thought to be helpful in the treatment of
                    Lou Gehrig’s disease (ALS), they are now considered problematic
                    for people with this disease. People with a condition called
                    branched-chain keto-aciduria (or Maple Syrup Urine Disease),
                    kidney disease, liver disease, heart disease, and people who drink
                    alcohol in excess should also avoid BCAA supplementation.
                </p>
